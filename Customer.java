package model;


public class Customer {

	public String name;
	public String phone;
	public String rentdate;
	public String returndate;
	
	public Customer(String name, String phone, String rentdate, String returndate) {
		this.name = name;
		this.phone = phone;
		this.rentdate = rentdate;
		this.returndate = returndate;
	}
	
}
